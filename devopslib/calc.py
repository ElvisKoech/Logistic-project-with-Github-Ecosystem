"""
A Calculator module

"""


def add(a, b):
    """Add two numbers"""
    return a + b


def sub(a, b):
    """Subtract two numbers"""
    return a - b


def mul(a, b):
    """Multiply two numbers"""
    return a * b


def div(a, b):
    """Divide two numbers"""
    return a / b


def power(a, b):
    """Calculate the power of a number with 'a' as the base and 'b' as the exponent"""
    return a**b
